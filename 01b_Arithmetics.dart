/*
  Some basic Arithmetic Operations. Add, Subtract, Multiply, Divide, & Divisor.
*/

void main() {
  int a = 10;
  int b = 5;
  int sum = a + b;
  int sub = a - b;
  double div = a / b;
  int divisor = a % b;
  int mult = a * b;

  print(sum);
  print(sub);
  print(div);
  print(divisor);
  print(mult);
}
