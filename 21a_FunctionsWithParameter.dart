void printingName(String name) {
  print("Name is " + name);
}

void main() {
  // Functions with Parameters in Dart
  print("Hello there !");
  String MyName = "TheAtifWaheed";
  // Saving Name into String Variable and then passing it to Function.
  printingName(MyName);

  // Another Way to Pass String...
  printingName("AtifWaheedKing");
}
